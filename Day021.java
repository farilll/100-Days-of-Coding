package Day021;

public class main {
    
    public static void main(String[] args) {
        // Tipe data char menjadi non-primitif Character
        Character nonPrimitiveChar = 'A';
        System.out.println("Tipe data non-primitif Character	: " + nonPrimitiveChar);

        // Tipe data boolean menjadi non-primitif Boolean
        Boolean nonPrimitiveBoolean = true;
        System.out.println("Tipe data non-primitif Boolean	  : " + nonPrimitiveBoolean);

        // Tipe data long menjadi non-primitif Long
        Long nonPrimitiveLong = 1234567890L;
        System.out.println("Tipe data non-primitif Long			  : " + nonPrimitiveLong);
    }
}
